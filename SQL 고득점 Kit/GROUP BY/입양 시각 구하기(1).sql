SELECT * FROM ANIMAL_OUTS;

SELECT 
    DATE_FORMAT(DATETIME, '%H') AS HOUR,
    COUNT(*) AS COUNT
FROM
    ANIMAL_OUTS
WHERE HOUR(DATETIME) BETWEEN '9' AND '19'
GROUP BY HOUR
ORDER BY 1;